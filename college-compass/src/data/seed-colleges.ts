function dl(
  collegeId: string,
  plan: CollegeDeadline["plan"],
  label: string,
  dueDate: string | null,
): CollegeDeadline {
  return {
    id: `${collegeId}-${plan}-${label.replace(/\s+/g, "-").toLowerCase()}`,
    collegeId,
    plan,
    label,
    dueDate,
    provenance: SAMPLE_PROV("deadlines"),
    mayHaveChanged: true,
  };
}

